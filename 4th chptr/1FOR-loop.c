#include<stdio.h>

int main(){
   // for loop:
   for(int i=0; i<=5; i++){
    printf("%d ", i);
     } printf("\n");
   
   for(int j=0; j>=-5; j--){
    printf("%d ", j);
     } printf("\n");

   for(float h=0; h<=5; h++){
    printf("%f ", h);
     } printf("\n");

   for(char ch='a'; ch<='e'; ch++){
    printf("%c  ", ch);
     } printf("\n");
   
    return 0;
}